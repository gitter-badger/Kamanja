package org.kamanja;

public class ValuePair {

	String valueType = "";
	Object value = "";

	public String getValueType() {
		return this.valueType;
	}

	public void setValueType(String valType) {
		this.valueType = valType;
	}

	public Object getValue() {
		return this.value;
	}

	public void setValue(Object val) {
		this.value = val;
	}
}
