package org.kamanja;

public class KeyValuePair {

	String key = "";
	ValuePair valuePair;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public ValuePair getValuePair() {
		return this.valuePair;
	}

	public void setValuePair(ValuePair valuePair) {
		this.valuePair = valuePair;
	}

}
